class Solution {
  public:
    vector<long long> factorialNumbers(long long n) {
        // Write Your Code here
        if(factorialNumbers(n)>n){
            return;
        }
        
        return factorialNumbers(n-1);
    }
};

//{ Driver Code Starts.
int main() {
    int t;
    cin >> t;
    while (t--) {
        long long N;
        cin >> N;
        Solution ob;
        vector<long long> ans = ob.factorialNumbers(N);
        for (auto num : ans) {
            cout << num << " ";
        }
        cout << endl;
        cout << "~" << endl;
    }
    return 0;
}